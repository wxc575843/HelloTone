package com.model;

public class Record {
	int id;
	String filePath;
	double score;
	String recordClass;
	String name;
	String picturePath;
	String explainPath;
	String examplePath;
	String filePathW;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	public String getRecordClass() {
		return recordClass;
	}
	public void setRecordClass(String recordClass) {
		this.recordClass = recordClass;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPicturePath() {
		return picturePath;
	}
	public void setPicturePath(String picturePath) {
		this.picturePath = picturePath;
	}
	public String getExplainPath() {
		return explainPath;
	}
	public void setExplainPath(String explainPath) {
		this.explainPath = explainPath;
	}
	public String getExamplePath() {
		return examplePath;
	}
	public void setExamplePath(String examplePath) {
		this.examplePath = examplePath;
	}
	public String getFilePathW() {
		return filePathW;
	}
	public void setFilePathW(String filePathW) {
		this.filePathW = filePathW;
	}
	
}
